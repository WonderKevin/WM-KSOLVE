with normalized_reference as (
  select distinct on (normalized_retailer_area, normalized_customer)
    normalized_customer,
    normalized_retailer_area,
    dc
  from (
    select
      btrim(regexp_replace(regexp_replace(regexp_replace(upper(replace(replace(coalesce(customer, ''), chr(160), ' '), '&', 'AND')), '#', '', 'g'), '[-_/(),.]', ' ', 'g'), '\s+', ' ', 'g')) as normalized_customer,
      btrim(regexp_replace(regexp_replace(regexp_replace(upper(replace(replace(coalesce(retailer_area, ''), chr(160), ' '), '&', 'AND')), '#', '', 'g'), '[-_/(),.]', ' ', 'g'), '\s+', ' ', 'g')) as normalized_retailer_area,
      dc
    from kehe_location_dc_reference
  ) ref
  order by normalized_retailer_area, normalized_customer, dc
), normalized_locations as (
  select
    id,
    btrim(regexp_replace(regexp_replace(regexp_replace(upper(replace(replace(coalesce(customer, ''), chr(160), ' '), '&', 'AND')), '#', '', 'g'), '[-_/(),.]', ' ', 'g'), '\s+', ' ', 'g')) as normalized_customer,
    btrim(regexp_replace(regexp_replace(regexp_replace(upper(replace(replace(coalesce(retailer_area, ''), chr(160), ' '), '&', 'AND')), '#', '', 'g'), '[-_/(),.]', ' ', 'g'), '\s+', ' ', 'g')) as normalized_retailer_area
  from public.locations
), matched_locations as (
  select loc.id, ref.dc
  from normalized_locations loc
  join normalized_reference ref
    on ref.normalized_customer = loc.normalized_customer
   and ref.normalized_retailer_area = loc.normalized_retailer_area
)
update public.locations as loc
set dc = matched.dc
from matched_locations matched
where loc.id = matched.id
  and coalesce(loc.dc, '') is distinct from matched.dc;

